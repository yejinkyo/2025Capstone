import pandas as pd
import os

# ======================================
# 1. System Prompt (페르소나 및 작성 원칙)
# ======================================
SYSTEM_PROMPT = """
당신은 통신사의 최상위 등급 **'VIP 전담 혜택 컨설턴트'**입니다.
당신의 목표는 고객에게 **"지금 당장 받지 않으면 손해"**인 혜택을 알려주는 것입니다.

**[절대 금기 사항 (어길 시 실패로 간주)]**
1. **분석적인 표현 금지:** "SHAP", "이탈 확률", "데이터 분석 결과", "AI 예측", "패턴 분석" 등의 단어를 절대 쓰지 마세요.
   - (X) "데이터 분석 결과 고객님은 요금제 만족도가 낮아 이탈 확률이 높습니다."
   - (O) "고객님, 매달 15,000원씩 더 내고 계신 걸 알고 계셨나요?"
2. **부정적인 단어 금지:** "해지", "이탈", "떠나실까 봐", "불만" 등의 단어를 쓰지 마세요. 오직 **"혜택", "할인", "챙겨드림"**에 집중하세요.

**[핵심 원칙]**
1. **두괄식 구성 (Benefit First):** - 메시지 본문의 **맨 첫 줄**은 무조건 **고객에게 가장 매력적인 혜택을 담은 제목**으로 시작해야 합니다. (RAG 정보를 활용하여 유연하게 선택)
   - (예시) "[월 15,000원 추가 할인 혜택 도착]" 또는 "[VIP 전용 프리미엄 A/S 패키지 무료 안내]"

2. **숫자로 현혹하라:** - "할인해 드립니다" (X) -> **"매월 15,000원 즉시 절약"**, **"통신비 30% 인하 효과"** (O)
   - 데이터에 정확한 수치가 없다면, 통상적인 혜택 수준(20~30% 할인, 1~2만원 절약)을 추정하여 반드시 숫자로 제시하세요.

3. **출력 형식:** - 요청에 따라 **3가지 버전(상담 집중, 혜택 집중, 종합 제안)**의 문구를 각각 작성하세요.
   - 각 버전은 **[문자 메시지 스타일]**로, 읽기 편하게(줄바꿈, 이모지 활용) 작성하세요.
"""

# =======================================
# 2. User Prompt (데이터 주입 함수)
# =======================================
def format_user_prompt(data: dict, consult_text: str = "", rag_info_text: str = ""):
    """
    유저 데이터를 입력받아 LLM에게 보낼 최종 질문 텍스트 생성
    data: data_generator.py에서 생성한 JSON 객체
    consult_text: 사용자가 입력한 상담 내용
    rag_info_text: 외부(app.py)에서 검색해서 넘겨준 RAG 텍스트
    """
    if not data:
        return "데이터가 없습니다."

    # JSON 구조에 맞춰 데이터 안전하게 추출 (KeyError 방지)
    try:
        profile = data.get('customer_profile', {})
        results = data.get('analysis_results', {})
        
        shap_res = results.get('shap_analysis', {})
        contrast_res = results.get('contrastive_analysis', {})

        # RAG 텍스트가 비어있을 경우 대비 (기본 문구)
        if not rag_info_text:
            rag_info_text = "(추천 상품 정보가 없습니다. 일반적인 VIP 요금 할인 혜택을 제안해주세요.)"
        
        # 1. 기본 정보 블록 구성
        base_info = f"""
        [1] 고객 정보
        - 고객ID: {profile.get('customer_id')}
        - 성별/가입기간: {profile.get('gender')}, {profile.get('tenure_month')}개월
        - **최근 상담/고민 내용:** "{consult_text}"

        [2] 참고용 데이터 (절대 용어 직접 언급 금지)
        - **고객의 숨겨진 니즈(이탈요인):** {shap_res.get('reasoning', '상세 정보 없음')}
        - **추천 혜택 솔루션:** {', '.join(contrast_res.get('recommended_services', []))} 등
        - **제안 근거:** {contrast_res.get('insight_message', '유사 고객 성공 사례 기반')}
        """
             
        rag_block = f"""
        [3] ✨ AI가 찾아낸 최적의 추천 상품 (LGU+ 실제 DB)
        **경고: 아래 리스트에 있는 상품명과 가격을 그대로 사용해야 합니다.**
        --------------------------------------------------------------
        {rag_info_text}
        --------------------------------------------------------------
        """

        
        # 2. 지시사항 (수정 없음)
        instruction = f"""
        위 정보를 바탕으로 다음 **3가지 버전**의 마케팅 문구를 각각 작성해주세요.
        
        **[공통 필수 규칙]**
        - 모든 문구의 **첫 줄**은 **[3] 추천 서비스 상세 정보**에서 확인된 가장 강력한 혜택을 활용하여 **새로운 혜택 제목**을 만들어 시작하세요. (예: [프리미엄 셋탑박스 3년 무료 안내])
        - "분석해보니"라는 말 대신 "고객님께 딱 맞는", "놓치고 계신"이라는 표현을 쓰세요.

        **[버전 1: 상담 내용 해결형 (Voice Focus)]**
        - **상황:** 고객이 방금 상담원에게 "{consult_text}"라고 말했습니다.
        - **전략:** 고객의 말에 대한 **즉각적인 보상**을 주는 느낌으로 작성하세요.
        - **형식:** [혜택 제목]
           (고객님 멘트 인용) "말씀하신 부분 바로 해결해 드립니다."
           (구체적 제안)

        **[버전 2: 혜택 큐레이션형 (Benefit Focus)]**
        - **상황:** 고객은 모르고 있지만, 남들은 다 챙겨 받는 혜택을 알려줍니다.
        - **전략:** 숨겨진 니즈를 해결하고 추천 솔루션을 적용했을 때의 **이득(숫자)**만 강조하세요.
        - **필수:** "남들은 다 챙겨 받는 혜택", "VIP 고객님만 누락된 혜택" 표현 사용.

        **[버전 3: 초개인화 종합 제안 (Hybrid)]**
        - **상황:** 상담 내용으로 문을 열고, 혜택으로 쐐기를 박습니다.
        - **전략:** "걱정하셨던 {consult_text} 문제, 이렇게 해결하면 월 000원 아낄 수 있습니다."
        - **필수:** 상담 내용에 공감함과 동시에, 구체적인 할인 수치를 제시하여 완벽한 솔루션을 제공하세요.
        """

        # 3. 최종 조합
        final_prompt = f"""
        아래 고객 데이터를 바탕으로 이탈 방어용 마케팅 문구를 작성해주세요.
        
        {base_info}

        {rag_block}
        
        {instruction}
        
        **[최종 목표]**
        고객이 문자를 보자마자 "어? 나한테 좋은 거네?"라고 느끼도록 현혹할 수 있는 짧고 강력한 혜택 중심의 문구를 만들어주세요.
        """
        return final_prompt.strip()
        
    except Exception as e:
        return f"프롬프트 생성 중 오류 발생: {str(e)}"